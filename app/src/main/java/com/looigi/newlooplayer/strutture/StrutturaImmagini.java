package com.looigi.newlooplayer.strutture;

public class StrutturaImmagini {
    private String Album;
    private String NomeImmagine;
    private String UrlImmagine;
    private String PathImmagine;
    private String CartellaImmagine;

    public String getAlbum() {
        return Album;
    }

    public void setAlbum(String album) {
        Album = album;
    }

    public String getNomeImmagine() {
        return NomeImmagine;
    }

    public void setNomeImmagine(String nomeImmagine) {
        NomeImmagine = nomeImmagine;
    }

    public String getUrlImmagine() {
        return UrlImmagine;
    }

    public void setUrlImmagine(String urlImmagine) {
        UrlImmagine = urlImmagine;
    }

    public String getPathImmagine() {
        return PathImmagine;
    }

    public void setPathImmagine(String pathImmagine) {
        PathImmagine = pathImmagine;
    }

    public String getCartellaImmagine() {
        return CartellaImmagine;
    }

    public void setCartellaImmagine(String cartellaImmagine) {
        CartellaImmagine = cartellaImmagine;
    }
}
